int main (void)
{
    WDTCTL = WDTPW | WDTHOLD;               // Stop watchdog timer

    /* Initialize */
    SYS_ConfigureOscillator();
    Init_App();
    Init_System();

    LED_DisplayShow();

    while(1)
    {
    	SLEEP();
    }
	return 0;
}

